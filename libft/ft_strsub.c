#include "libft.h"

char	*ft_strsub(const char *s, unsigned int start, size_t len)
{
	char			*sub;
	unsigned int	i;

	sub = ft_strnew(len);
	if (!sub)
		return (NULL);
	i = 0;
	while (i < len)
	{
		sub[i] = s[start];
		start++;
		i++;
	}
	return (sub);
}
